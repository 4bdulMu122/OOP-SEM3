public void EnterNumbers(String q) {
    String currentText = jtxtResult.getText();
    // Reset if the current text is a result or if the input is empty
    if (currentText.equals("0") || (op != null && !currentText.contains(" "))) {
        jtxtResult.setText(q);
    } else {
        jtxtResult.setText(currentText + q);
    }
}
