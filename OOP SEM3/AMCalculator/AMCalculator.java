    private void jbtnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEqualActionPerformed
        String[] parts = jtxtResult.getText().split(" ");
        if (parts.length == 3) {
            NumEnter2 = Double.parseDouble(parts[2]); // Get the second number
            switch (op) {
                case "+":
                    Result = NumEnter1 + NumEnter2;
                    break;
                case "-":
                    Result = NumEnter1 - NumEnter2;
                    break;
                case "*":
                    Result = NumEnter1 * NumEnter2;
                    break;
                case "/":
                    Result = NumEnter1 / NumEnter2;
                    break;
            }
        jtxtResult.setText(jtxtResult.getText() + " = " + Result);
        op = null; // Reset operator after calculation
    }        
    }//GEN-LAST:event_jbtnEqualActionPerformed

